<script>

    (function($) {

        $(document).ready(function() {


            $("#main_gallery").find("div").click(function () {

                $(this).closest("section").find("#gallery_view").addClass("activeClass");
            });

            slickSlider();

            $(".slick-arrow").click(function() {

                var alt =  $(this).siblings(".slick-list").find(".slick-active img").attr("alt");

                $(this).closest("#gallery_view").find(".image_title").text(alt);
            });

            $(".exit").click(function () {
                $(this).closest("#gallery_view").removeClass("activeClass");
            });
        });

        function slickSlider(){
            $('.js_gallery').slick({
                infinite: true,
                speed: 300,
                slidesToShow: 1,
                adaptiveHeight: true
            });
        }
    })(jQuery);

